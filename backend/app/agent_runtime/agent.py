"""AgentRuntime — Microsoft Agent Framework SDK orchestrator.

Pipeline (shared by both streaming and non-streaming paths):

  1. Resolve conversation ownership:
       a. If thread_id was client-provided: the conversation MUST already exist
          for this user (routes.py already validated this, but we guard
          defensively).  If not found, abort — no persist attempt is made into
          a non-existent conversation.
       b. If thread_id was auto-generated by AgentSession: create the
          conversation record now (backward-compatible flow for callers that
          do not pre-create via POST /conversations).
  2. Persist user message → receive MessageRecord with reserved sequence number.
     If persistence fails, the pipeline continues in degraded mode (no history
     stored) with an explicit WARNING log.
  3. RETRIEVE — hybrid Azure AI Search (keyword + VectorizedQuery)
  4. GATE — abort if evidence count or avg score is below threshold
  5. Get or create AF session (keyed by (user_id, thread_id) composite key)
     └─ Cold-start: load Cosmos history BEFORE the just-saved user message
        (using before_sequence filter) and inject once via CosmosHistoryProvider.
        This prevents the current question from appearing in both the injected
        history block and the active user prompt.
     └─ Warm session: skip history injection; InMemoryHistoryProvider already
        has the in-process turn history.
  6. Inject pre-retrieved results via RagContextProvider
  7. GENERATE via Agent Framework ChatAgent (streaming internally, always)
  8. Emit structured citations from retrieval results (no text-pattern gating)
  9. Persist assistant message + citations

run_stream()  — wraps the pipeline and yields SSE-formatted strings.
run_once()    — wraps the pipeline and returns a plain dict (no SSE).
               Used by POST /chat so the route never parses SSE text.

Session cache:
  AF sessions are keyed by (user_id, thread_id) — never by thread_id alone.
  This prevents different users from accidentally sharing a warm session even
  if they somehow have colliding thread IDs.

  Sessions are evicted after _SESSION_TTL_SECONDS of inactivity and the cache
  is capped at _SESSION_MAX_ENTRIES entries.  This prevents unbounded memory
  growth in long-running processes.  Eviction happens at the start of every
  new session lookup — no background task is needed.

Multi-user isolation:
  Ownership of the client-supplied thread_id is validated in routes.py before
  the runtime is invoked.  The runtime additionally guards defensively so that
  even without the route-level check, a missing conversation for the current
  user causes a clean abort rather than silently creating a shadow record.
  Message history is loaded via get_messages_for_user() which enforces both
  conversation ownership and per-user message filtering.
"""

